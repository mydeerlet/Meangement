package bean;

public class SecondCarBean {

	private String name;
	private String number;
	private String ID_number;
	private int marry;
	private String motorcycle_type;
	private String unit;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getID_number() {
		return ID_number;
	}
	public void setID_number(String iD_number) {
		ID_number = iD_number;
	}
	public int getMarry() {
		return marry;
	}
	public void setMarry(int marry) {
		this.marry = marry;
	}
	public String getMotorcycle_type() {
		return motorcycle_type;
	}
	public void setMotorcycle_type(String motorcycle_type) {
		this.motorcycle_type = motorcycle_type;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	
	
}
